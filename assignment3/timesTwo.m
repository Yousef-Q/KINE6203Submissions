function [result] = timesTwo(num1)
%timesTwo takes any number entered into the function and doubles that number
%by multiplying it by 2. 
%
%Input = one number 
%Output = Number x 2 
%Written by Yousef Qadumi 
%Tested on MatLab v. 2024a
%Ok on Mac OS Sequoia v. 15.0
